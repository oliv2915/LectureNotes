// equal to
// does not check data type but only value
let equalTo = "3" == 3;
console.log(equalTo);

// strict equal to
// checks data type and value
let strictEqualsTo = "3" === 3;
console.log(strictEqualsTo);

// not equal to
// does not check data type but only value
let notEqualTo = 3 != "3";
console.log(notEqualTo);

// strict not equal to
// checks data type and value
let strictNotEqualTo = 3 !== "3";
console.log(strictNotEqualTo);

// greater than
let greaterThan = 4 > 3;
console.log(greaterThan);

// less than
let lessThan = 33 < 100;
console.log(lessThan);

// greather than or equal to
let greaterThanOrEqualTo = 3 >= 2;
console.log(greaterThanOrEqualTo);

// less than or equal to
let lessThanOrEqualTo = 2 <= 3;
console.log(lessThanOrEqualTo);

// and
2 + 2 && 3 + 3

// or
2 || 3